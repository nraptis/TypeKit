import Foundation

public struct TanHandles {
    public let inX: Float
    public let inY: Float
    public let outX: Float
    public let outY: Float
    public init(inX: Float, inY: Float, outX: Float, outY: Float) {
        self.inX = inX
        self.inY = inY
        self.outX = outX
        self.outY = outY
    }
}
